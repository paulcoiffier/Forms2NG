public class FormsStats {

    private int numberOfBlocks;
    private int numberOfTriggers;
    private int numberOfItems;
    private int numberOfCanvas;

    public FormsStats() {
    }

    public int getNumberOfBlocks() {
        return numberOfBlocks;
    }

    public void setNumberOfBlocks(int numberOfBlocks) {
        this.numberOfBlocks = numberOfBlocks;
    }

    public int getNumberOfTriggers() {
        return numberOfTriggers;
    }

    public void setNumberOfTriggers(int numberOfTriggers) {
        this.numberOfTriggers = numberOfTriggers;
    }

    public int getNumberOfItems() {
        return numberOfItems;
    }

    public void setNumberOfItems(int numberOfItems) {
        this.numberOfItems = numberOfItems;
    }

    public int getNumberOfCanvas() {
        return numberOfCanvas;
    }

    public void setNumberOfCanvas(int numberOfCanvas) {
        this.numberOfCanvas = numberOfCanvas;
    }
}
